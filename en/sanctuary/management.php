                                                                    <div style="width:400px;height:175px;padding:25px;overflow:auto;">
      
      <p>RAKWS was established in 1985 and was officially declared as a protected area on March 1, 1998 by Local Order No. (2) of 1998. In December 2003, His Highness, the Ruler of Dubai has promulgated Law No. 11 of 2003 on the Establishment of Protected Areas in the Emirate of Dubai giving  RAKWS  full  protection from Dubai Municipality.</p>

<p>On August 29, 2007, RAKWS was declared as the UAE's first RAMSAR Site. It was officially accepted on December 29, 2007 where the UAE become the 156th member of the RAMSAR Convention. It is also identified as a globally Important Bird Area (IBA) by Birdlife International and considered an exceptional wetland within UAE.
</p>                                                                  <p><strong>The specific role of Dubai Municipality in environmental conservation is to:</strong></p>
                                                                        <ul style="line-height:25px;">
                                                                            <li>Prepare conservation plans and legislation</li>
                                                                            <li>Conduct research and studies</li>
                                                                            <li>Conserve and restore the biodiversity of the Emirate of Dubai</li>
                                                                            <li>Ensure the protection of plants and animal stocks of actual or potential value to mankind</li>
                                                                            <li>Help the preservation of species and ecological processes that underline rural productivity and the restoration of ecosystems degraded by unwise land use</li>
                                                                            <li>Ensure protection of conservation areas in accordance with Law No. 11 of 2003 on protected areas</li>
                                                                            <li>Enforce laws on the violators</li>
                                                                        </ul>
                                                                        <p><strong>The main objectives of the RAKWS management are to:</strong></p>
                                                                        <ul style="line-height:25px;">
                                                                            <li>Conserve and improve the biological diversity of coastal and terrestrial ecosystems typical of the intertidal area along the shores of Ras Al Khor</li>
                                                                            <li>Maintain the essential ecological processes within these systems</li>
                                                                            <li>Manage their renewable resources sustainably</li>
                                                                            <li>Undertake studies and research to generate data for better management of the Sanctuary</li>
                                                                            <li>Protect and restore the faunal and floral diversity, as well as natural abundance of individual species through habitat conservation, management and restoration</li>
                                                                            <li>Educate the community about natural heritage, general principles of conservation and the sustainable use of biological resources</li>
                                                                            <li>Develop and implement an ecotourism programme in the wilderness area</li>
                                                                        </ul>
