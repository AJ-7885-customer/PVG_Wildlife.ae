                            <p> The United Arab Emirates (UAE) has ratified the Ramsar Convention when the UNESCO, the Ramsar Convention's legal depositary, announced that its partnership to the Convention will enter into force on 29 December 2007. </p>

                            <p><h3> What is a Ramsar site?</h3> </p>	
                            <p>Ramsar sites are wetlands of international importance designated under the Ramsar Convention. The convention was developed and adopted by contracting nations at a meeting in Ramsar, Iran on February 2, 1971 and came into force on December 21, 1975. In January 2011, The Ramsar List of Wetlands of International Importance included 160 contracting parties covering over 1,900 sites (known as Ramsar sites) with a total area of over 187,984,550 km², up from 1,021 sites.</p>
                            <p>For the latest updates check Ramsar website: <a href="https://www.ramsar.org/cda/en/ramsar-home/main/ramsar/1_4000_0__" target="_new">www.ramsar.org</a></p>
                            <p><h3>UAE's first Ramsar site</h3></p>
                            <p>With The UAE's accession, Ras Al Khor Wildlife Sanctuary (RAKWS) has become the Nation's first Ramsar wetland site. RAKWS is located at the head of the 14Km long watercourse known as Dubai Creek, and covers an area of 620 hectares featuring sabkhas saline flats, intertidal mudflats and mangroves, small lagoons and pools, and a few tiny islands which lies at the interface between the Arabian Gulf and the Al Awir Desert.</p> 
                            <p>During winter, RAKWS supports more than 20,000 water birds of 67 species and acts as a critical staging ground for the wintering birds of the East African-West Asian Flyway. The site hosts more than 500 species of flora and fauna and is one of the best-managed arid zone wetlands in the region. Located within Dubai city, it is an important eco-tourism destination and receives increasing numbers of local and international visitors.</p> 
                            <p>The importance of RAKWS is beyond its aesthetic value. It serves as a beacon on the significance of biodiversity as a life supporting system for humanity's continual existence and its being an integral part of the cultural and traditional heritage of the UAE.
